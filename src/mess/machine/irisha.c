static TIMER_CALLBACK( irisha_reset )
{
    memory_set_bank(1, 0);
}

MACHINE_RESET( irisha )
{
    timer_set(ATTOTIME_IN_USEC(10), NULL, 0, irisha_reset);
    memory_set_bank(1, 1);
    ppi8255_init(&irisha_ppi8255_interface);
    irisha_keyboard_line = 0;
}


