WRITE8_HANDLER( irisha_tape_w )
{
    cassette_output(image_from_devtype_and_index(IO_CASSETTE, 0),data & 0x01 ? 1 : -1);
}


READ8_HANDLER( irisha_tape_r )
{
    double level = cassette_input(image_from_devtype_and_index(IO_CASSETTE, 0));
    if (level <  0) {
            return 0x00;
    }
    return 0xff;
}
