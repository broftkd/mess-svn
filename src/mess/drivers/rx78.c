static VIDEO_UPDATE( rx78 )
{
	UINT8 y,ra,chr,gfx;
	UINT16 sy=0,ma=0xe8be,x;
	UINT8 *RAM = memory_region(screen->machine, "maincpu");

	/* TODO: screw this up */
	for (y = 0; y < 23; y++)
	{
		for (ra = 0; ra < 8; ra++)
		{
			UINT16  *p = BITMAP_ADDR16(bitmap, sy++, 0);

			for (x = ma; x < ma+30; x++)
			{
				chr = RAM[x];

				/* get pattern of pixels for that character scanline */
				gfx = RAM[0x1a27 + (chr<<3) + ra];

				/* Display a scanline of a character (8 pixels) */
				*p = ( gfx & 0x01 ) ? 1 : 0; p++;
				*p = ( gfx & 0x02 ) ? 1 : 0; p++;
				*p = ( gfx & 0x04 ) ? 1 : 0; p++;
				*p = ( gfx & 0x08 ) ? 1 : 0; p++;
				*p = ( gfx & 0x10 ) ? 1 : 0; p++;
				*p = ( gfx & 0x20 ) ? 1 : 0; p++;
				*p = ( gfx & 0x40 ) ? 1 : 0; p++;
				*p = ( gfx & 0x80 ) ? 1 : 0; p++;
			}
		}
		ma+=30;
	}
	return 0;
}

static UINT8 key_mux;

static READ8_HANDLER( key_r )
{
	static const char *const keynames[] = { "KEY0", "KEY1", "KEY2", "KEY3",
	                                        "KEY4", "KEY5", "KEY6", "KEY7",
	                                        "KEY8", "JOY1P_0","JOY1P_1","JOY1P_2",
	                                        "JOY2P_0", "JOY2P_1", "JOY2P_2", "UNUSED" };

	if(key_mux == 0x30) //status read
	{
		int res,i;

		res = 0;
		for(i=0;i<15;i++)
			res |= input_port_read(space->machine, keynames[i]);

		return res;
	}

	if(key_mux >= 1 && key_mux <= 9)
		return input_port_read(space->machine, keynames[key_mux - 1]);

	return 0;
}

static WRITE8_HANDLER( key_w )
{
	key_mux = data;
}


